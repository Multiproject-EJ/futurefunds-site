export function getEditorDefaultModels() {
  return DEFAULT_MODEL_CATALOG.map((model, index) => ({
    value: model.slug,
    label: model.label,
    is_default: index === 0 || Boolean(model.is_default),
  }));
}

export function getPlannerFallbackModels() {
  return DEFAULT_MODEL_CATALOG.map(({ slug, label, provider, tier, price_in, price_out, notes }) => ({
    slug,
    label,
    provider,
    tier,
    price_in,
    price_out,
    notes: notes || null,
  }));
}
